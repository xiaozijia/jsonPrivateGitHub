    /**
     * 获取汉字的拼音首字母
     */
    public static String getPinyinFirstLetter(String chinese) {
        if (chinese == null || chinese.length() == 0) {
            return "Z";
        }

        char c = chinese.charAt(0);
        
        // 如果是英文字母，直接返回
        if (c >= 'A' && c <= 'Z') {
            return String.valueOf(c);
        }
        if (c >= 'a' && c <= 'z') {
            return String.valueOf(c).toUpperCase();
        }

        // 如果是汉字，获取拼音首字母
        int code = c;
        for (int i = 0; i < PINYIN_CODE.length; i++) {
            if (code >= PINYIN_CODE[i] && code < PINYIN_CODE[i + 1]) {
                return PINYIN_TABLE[i];
            }
        }

        return "Z";
    }
